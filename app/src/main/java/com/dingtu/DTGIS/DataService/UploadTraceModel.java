package com.dingtu.DTGIS.DataService;

public class UploadTraceModel extends GPSPoint {

	private double x;
	public void setX(double x)
	{
		this.x = x;
	}
	public double getX()
	{
		return this.x;
	}
	
	private double y;
	public void setY(double y)
	{
		this.y = y;
	}
	public double getY()
	{
		return y; 
	}
}
